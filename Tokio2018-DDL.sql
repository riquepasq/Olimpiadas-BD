    CONSTRAINT FKROTINAPREPARADOR FOREIGN KEY (Preparador)
        REFERENCES PREPARADOR(NroPreparador) ON DELETE CASCADE,
    CONSTRAINT FKROTINAATLETA FOREIGN KEY (Atleta)
        REFERENCES ATLETA(NroAtleta) ON DELETE CASCADE
);

